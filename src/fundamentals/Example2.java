package fundamentals;

public class Example2 {
	/*
	 * 
	 * println() method will print the message and then go to next line, hence any other message to be printed will
	 * be done on next line.
	 * 
	 * print() method will print the message and then stay on that line itself, hence any other message to be printed
	 * will be done in continuation on the same line.
	 */
	public static void main(String[] args) {
		
		System.out.println("Hello everyone!!! Hope you all are doing great!!!");
		System.out.print("Today is thursday!!");
		System.out.println("Sujina Pradeep is the java faculty for this batch");
		
		System.out.println("There are many students in my class\nRajat\nAlka\nRitu\nVishakha");
	}

}
