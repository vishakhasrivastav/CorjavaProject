package stringPrograms;

public class CharAtEx1 {

	public static void main(String[] args) {
		/*
		 * charAt(index) method returns the character of the string present at the 
		 * given index.
		 */
		
		String s="Anudip";
		int l=s.length();
		
		System.out.println("Length of String= "+l);
		
		char ch=s.charAt(0);
		System.out.println("character at index 3: "+ch);
		
		

	}

}
