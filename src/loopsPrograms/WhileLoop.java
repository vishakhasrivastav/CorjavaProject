package loopsPrograms;

public class WhileLoop {

	public static void main(String[] args) {
		/*
		 * while loops are used when we do not know the exact number of 
		 * iterations/times the loop needs to run.
		 * 
		 * Syntax:-
		 * initialization
		 * 
		 * while(condition){
		 * 
		 * increment/decrement
		 * 
		 * }
		 */
		int i=1;
		while(i<=10) {
			
			System.out.println(i);
			i++;
		}

	}

}
