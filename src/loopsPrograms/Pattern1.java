package loopsPrograms;

public class Pattern1 {

	public static void main(String[] args) {
		
		/* 1)
		 *  * * * * *
		 *  * * * * *
		 *  * * * * *
		 *  * * * * *
		 *  * * * * *
		 *  
		 
		 */
		
		for(int i=1; i<=5; i++) {
			
			for(int a=1; a<=5; a++) {
			
				System.out.print("* ");
			}
		System.out.println();
		
		}

	}

}
